SELECT LP.*,  
 CAST(HSP2_2.DATA_HORA AS datetime) AS DATA_PICKING_LIST_EMITIDO,  
 CAST(HSP2_3.DATA_HORA AS datetime) AS DATA_EM_SEPARACAO,  
 CAST(HSP2.DATA_HORA AS datetime) AS DATA_EMBALADO,  
 CAST(HSP10.DATA_HORA AS datetime) AS DATA_DUPLOCHECK,  
 CAST(ISNULL(LV.DATA_DIGITACAO,HSP3.DATA_HORA) AS datetime) AS DATA_FATURAMENTO,  
 CAST(ISNULL(HSP5.DATA_HORA,HSP6.DATA_HORA) AS datetime) AS DATA_EXPEDIDO,  
 CASE WHEN HSP4.DATA_HORA IS NOT NULL THEN 'CANCELADO' ELSE 'VÁLIDO' END AS STATUS_PEDIDO,  
 MONTH(LP.DATA_VENDA) AS MES,  
 (DAY(LP.DATA_VENDA) + (DATEPART(DW, DATEADD (MONTH, DATEDIFF (MONTH, 0, LP.DATA_VENDA), 0))-1)-1)/7 + 1 AS SEMANA_MES,  
 YEAR (LP.DATA_VENDA)  AS ANO,  
 LNF.NF_NUMERO,  
 LNF.SERIE_NF,  
 LNF.CHAVE_NFE,  
 ISNULL(LV.VALOR_TIKET,LP.VALOR_TOTAL) AS VALOR,  
 LNF.VOLUMES,  
 LNF.PESO_BRUTO,  
 CV.UF as UF_DESTINO,  
 'RJ' AS UF_ORIGEM  
   
  
FROM (   
  
 SELECT   
    
  'HUB SP' AS SITE,  
  'ECOMM CRISBARROS' AS GRUPO,  
  'ECOMMERCE' AS CANAL,  
  'CRIS BARROS' AS MARCA,  
  LP.CODIGO_FILIAL_ORIGEM,  
  c.FILIAL AS NOME_FILIAL_INDICADORES,  
  LP.PEDIDO,  
  CASE WHEN MAX(CASE WHEN P.PRODUTO LIKE'12%' THEN 1 ELSE 0  END) = 1 THEN 'SIM' ELSE 'NAO' END AS POSSUI_JOIAS,  
  SUM(LPP.QTDE) AS QTDE_TOTAL,  
  HSP1.DATA_HORA AS DATA_VENDA,  
  CASE WHEN DIGITACAO_ENCERRADA = 0 THEN 'NAO' ELSE 'SIM' END AS DIGITACAO_ENCERRADA,  
  CASE WHEN UPPER(LP.FORMA_ENVIO) LIKE '%FARFETCH%'  THEN 'SIM' ELSE 'NAO'   END AS TIPO_FARFETH,  
  UPPER(LP.FORMA_ENVIO) AS TRANSPORTADORA,  
  LP.VALOR_TOTAL,  
  MAX(CAST(LPP.DATA_PREVISAO_ENTREGA AS DATE)) AS DATA_PREVISAO_ENTREGA,  
  LP.PEDIDO_SITE  
   
    
 FROM [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[LOJA_PEDIDO] LP (NOLOCK)  
    
    
 JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[LOJA_PEDIDO_PRODUTO] LPP (NOLOCK)  
 ON LPP.PEDIDO = LP.PEDIDO    
 AND LPP.PRODUTO <>'99999999'  
 AND LPP.QTDE > 0  
 AND LPP.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  
LEFT JOIN  [PRODUTOS] P (NOLOCK)  
 ON LPP.PRODUTO = P.PRODUTO  COLLATE SQL_LATIN1_GENERAL_CP1_CI_AS  
   
JOIN  FILIAIS C (NOLOCK)  
 ON LP.CODIGO_FILIAL_ORIGEM = C.COD_FILIAL  COLLATE SQL_LATIN1_GENERAL_CP1_CI_AS  
  
JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP1 (NOLOCK)  
  ON HSP1.PEDIDO = LP.PEDIDO  
  AND HSP1.CODIGO_STATUS_PEDIDO = 1  
  AND HSP1.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM /*INICIO SEPARACAO*/  
  
WHERE CAST(ISNULL(LP.DATA_HORA_VENDA,LP.DATA) AS DATE) >= @DATAINICIO AND CAST(ISNULL(LP.DATA_HORA_VENDA,LP.DATA) AS DATE) <= @DATAFIM  
AND LPP.PRODUTO <> '888888'  
  
GROUP BY  
    
  LP.CODIGO_FILIAL_ORIGEM,  
  --C.NOME_FILIAL_INDICADORES,  
  LP.PEDIDO,  
  HSP1.DATA_HORA,  
  CASE WHEN DIGITACAO_ENCERRADA = 0 THEN 'NAO' ELSE 'SIM' END ,  
  CASE WHEN UPPER(LP.FORMA_ENVIO) LIKE '%FARFETCH%'  THEN 'SIM' ELSE 'NAO'   END ,  
  UPPER(LP.FORMA_ENVIO),  
  LP.VALOR_TOTAL,  
  LP.PEDIDO_SITE,  
  C.FILIAL  
      
   )LP  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP2_2 (NOLOCK)  
  ON HSP2_2.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP2_2.PEDIDO = LP.PEDIDO  
  AND HSP2_2.CODIGO_STATUS_PEDIDO = 2 AND HSP2_2.REIMPRESSAO = 0  /*PICKING LIST EMITIDO*/  
  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP2_3 (NOLOCK)  
  ON HSP2_3.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP2_3.PEDIDO = LP.PEDIDO  
  AND HSP2_3.CODIGO_STATUS_PEDIDO = 3 /*EM SEPARACAO*/  
        
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP2 (NOLOCK)  
  ON HSP2.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP2.PEDIDO = LP.PEDIDO  
  AND HSP2.CODIGO_STATUS_PEDIDO = 4 /*EMBALADO*/  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP3 (NOLOCK)  
  ON HSP3.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP3.PEDIDO = LP.PEDIDO  
  AND HSP3.CODIGO_STATUS_PEDIDO = 5  
  AND HSP3.REIMPRESSAO = 0  /*FATURADO*/  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP4 (NOLOCK)  
  ON HSP4.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP4.PEDIDO = LP.PEDIDO  
  AND HSP4.CODIGO_STATUS_PEDIDO = 9  /*CANCELAMENTO*/  
    
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP5 (NOLOCK)  
  ON HSP5.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP5.PEDIDO = LP.PEDIDO  
  AND HSP5.CODIGO_STATUS_PEDIDO = 7   
  AND HSP5.REIMPRESSAO = 0 /*EXPEDIDO*/  
    
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP6 (NOLOCK)  
  ON HSP6.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP6.PEDIDO = LP.PEDIDO  
  AND HSP6.CODIGO_STATUS_PEDIDO = 8   
  AND HSP6.REIMPRESSAO = 0 /*INTEGRADO SITE / TRANSP*/  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[SS_HISTORICO_STATUS_LOJA_PEDIDO] HSP10 (NOLOCK)  
  ON HSP10.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
  AND HSP10.PEDIDO = LP.PEDIDO  
  AND HSP10.CODIGO_STATUS_PEDIDO = 11  
  AND HSP10.REIMPRESSAO = 0 /*DUPLOCHECK*/  
  
LEFT JOIN ( SELECT DISTINCT(PEDIDO) AS PEDIDO, TICKET,DATA_VENDA, CODIGO_FILIAL_ORIGEM FROM [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[loja_pedido_venda] LPV (NOLOCK) WHERE DATA_VENDA >= @DATAINICIO  
  
) LPV  
ON LPV.PEDIDO = LP.PEDIDO  
AND LPV.CODIGO_FILIAL_ORIGEM = LP.CODIGO_FILIAL_ORIGEM  
       
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[loja_venda] LV (NOLOCK)  
  ON LV.TICKET = LPV.TICKET collate SQL_Latin1_General_CP1_CI_AS  
  AND LPV.DATA_VENDA = LV.DATA_VENDA  
  AND LV.CODIGO_FILIAL = LP.CODIGO_FILIAL_ORIGEM  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO]. [loja_venda_pgto] LVP (NOLOCK)  
  ON LV.lancamento_caixa = LVP.lancamento_caixa collate SQL_Latin1_General_CP1_CI_AS  
  AND LV.TERMINAL_PGTO = LVP.TERMINAL  
  AND LVP.CODIGO_FILIAL = LP.CODIGO_FILIAL_ORIGEM  
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[clientes_varejo] CV (NOLOCK)  
  ON CV.codigo_cliente = LV.codigo_cliente collate SQL_Latin1_General_CP1_CI_AS    
  
LEFT JOIN [192.168.9.205].[ECOMMCRISBARROSVTEX].[DBO].[LOJA_NOTA_FISCAL]  LNF (NOLOCK)  
  ON LNF.nf_numero = LVP.NUMERO_FISCAL_VENDA collate SQL_Latin1_General_CP1_CI_AS  
  AND LNF.EMISSAO = LVP.DATA     
  AND LNF.CODIGO_FILIAL = LP.CODIGO_FILIAL_ORIGEM  