CREATE PROCEDURE [dbo].[PIT_SalvaAlocacaoOP](@PEDIDO VARCHAR(8), @ORDEM_PRODUCAO VARCHAR(8), @MATERIAL VARCHAR(12), @COR_MATERIAL VARCHAR(12),   
@NF_CONTA_ORDEM VARCHAR(10), @SERIE_CONTA_ORDEM VARCHAR(3), @QTDE_ALOCAR NUMERIC(18,3), @MATAR_SALDO BIT, @CODIGO_RETORNO INT OUTPUT, @MENSAGEM VARCHAR(3000) OUTPUT)   
AS  
DECLARE @REQ_MATERIAL VARCHAR(8), @FACCAO VARCHAR(25), @FILIAL VARCHAR(25), @VALOR NUMERIC(18,5), @CUSTO NUMERIC(18,5), @ITEM VARCHAR(3), @FORNECEDOR VARCHAR(25),
@NOTA_COBERTURA VARCHAR(10), @SERIE_COBERTURA VARCHAR(6), @FILIAL_COBERTURA VARCHAR(25), @CLIENTE VARCHAR(25), @PECA VARCHAR(6), @NF_V VARCHAR(10), @FORNEC_V VARCHAR(25),
@REQ_MAT_V VARCHAR(10), @CODIGO_ESPELHO BIGINT, @FILIAL_TRANSITO VARCHAR(25)   
 

  
BEGIN TRANSACTION      
  
SELECT @CLIENTE = VAL_PARAMETRO from PITAGORAS_BD.dbo.PARAMETROS WHERE PARAMETRO = 'PAR_EMPRESA' --[MF]


SELECT @CODIGO_RETORNO = 0, @MENSAGEM = ''   


SELECT @FILIAL_TRANSITO = ISNULL(VALOR_ATUAL,'') FROM PARAMETROS WHERE PARAMETRO = 'PIT_FILIAL_TRANSITO'

SELECT @FACCAO = FACCAO, @FORNECEDOR = FORNECEDOR, @NOTA_COBERTURA = NF_REMESSA_SIMBOL,  
@SERIE_COBERTURA = SERIE_NF_REMESSA_SIMBOL, @CODIGO_ESPELHO = CODIGO_ESPELHO 
FROM PITAGORAS_BD.DBO.XML_PIT_NF_COMPRA WHERE TIPO_XML = 'R' AND PEDIDO = @PEDIDO AND NF_ENTRADA = @NF_CONTA_ORDEM  

  
--IDENTIFICO SE POSSUI FILIAL UNICA DE TRIANGULACAO  
IF EXISTS(SELECT 1 FROM PARAMETROS WHERE PARAMETRO = 'PIT_FILIAL_TRIANGULACAO' AND ISNULL(VALOR_ATUAL,'') <> '')  
	SELECT @FILIAL = VALOR_ATUAL FROM PARAMETROS WHERE PARAMETRO = 'PIT_FILIAL_TRIANGULACAO'  
ELSE   
	SELECT @FILIAL = @FACCAO   
  
EXEC LX_SEQUENCIAL 'ESTOQUE_SAI_MAT_00.REQ_MATERIAL', 1, @REQ_MATERIAL OUTPUT      
SELECT @ITEM = '001'  
  
--INSIRO A CAPA DA REQUISICAO   
BEGIN TRY   

	select @REQ_MATERIAL, @FILIAL, 'B', CAST(GETDATE() AS DATE), suser_name(), @FACCAO,  
	@PEDIDO, 0, CAST(GETDATE() AS DATE), '014', 'ALOCACAO DE TRIANGULACAO PEDIDO MP: ' + LTRIM(RTRIM(@PEDIDO)) 
	+ ', NOTA DE CONTA E ORDEM: ' + LTRIM(RTRIM(@NF_CONTA_ORDEM)) + '-' + LTRIM(RTRIM(@SERIE_CONTA_ORDEM)) + ' DE ' + LTRIM(RTRIM(@FORNECEDOR))
	+ ', NOTA DE COBERTURA: ' + + LTRIM(RTRIM(@NOTA_COBERTURA)) + '-' + LTRIM(RTRIM(@SERIE_COBERTURA)) 
	, @NF_CONTA_ORDEM, @SERIE_CONTA_ORDEM


	INSERT INTO ESTOQUE_SAI_MAT (REQ_MATERIAL,FILIAL,TIPO_MOVIMENTACAO,EMISSAO,RESPONSAVEL,REQUISITANTE,  
	DESTINO,CAMBIO_NA_DATA,DATA_DIGITACAO,CM_OPERACAO, OBS, NF_ENTRADA, SERIE_NF_ENTRADA)   
	VALUES (@REQ_MATERIAL, @FILIAL, 'B', CAST(GETDATE() AS DATE), suser_name(), @FACCAO,  
	@PEDIDO, 0, CAST(GETDATE() AS DATE), '014', 'ALOCACAO DE TRIANGULACAO PEDIDO MP: ' + LTRIM(RTRIM(@PEDIDO)) 
	+ ', NOTA DE CONTA E ORDEM: ' + LTRIM(RTRIM(@NF_CONTA_ORDEM)) + '-' + LTRIM(RTRIM(@SERIE_CONTA_ORDEM)) + ' DE ' + LTRIM(RTRIM(@FORNECEDOR))
	+ ', NOTA DE COBERTURA: ' + + LTRIM(RTRIM(@NOTA_COBERTURA)) + '-' + LTRIM(RTRIM(@SERIE_COBERTURA)) 
	, @NF_CONTA_ORDEM, @SERIE_CONTA_ORDEM)  
END TRY  
   
BEGIN CATCH   
	SELECT @CODIGO_RETORNO = 2,   
	@MENSAGEM = ';Erro ao salvar a capa da requisição de saída! Informe o setor de TI. '  + ERROR_MESSAGE()   
  
	GOTO ERROR  
END CATCH     
  
SELECT @CUSTO = CUSTO, @VALOR = CUSTO * @QTDE_ALOCAR  
FROM VW_PIT_AlocacaoTriangulacao   
WHERE Pedido = @PEDIDO   
AND Faccao = @FACCAO  
AND Material = @MATERIAL  
AND CorMaterial = @COR_MATERIAL   
AND NfContaOrdem = @NF_CONTA_ORDEM  
   
--INSIRO O ITEM DA REQUISICAO  
BEGIN TRY   

	select @REQ_MATERIAL, @FILIAL, @MATERIAL, @COR_MATERIAL, @ITEM, @ORDEM_PRODUCAO, @QTDE_ALOCAR, @QTDE_ALOCAR, @CUSTO, @CUSTO, @VALOR, 1, '', @VALOR, NULL, @MATAR_SALDO


	INSERT INTO ESTOQUE_SAI1_MAT (REQ_MATERIAL, FILIAL, MATERIAL, COR_MATERIAL, ITEM, ORDEM_PRODUCAO, QTDE, QTDE_AUX,   
	CUSTO, PRECO, VALOR, FATOR_CONVERSAO_UNIDADE, UNIDADE_FATURAMENTO, VALOR_BRUTO, ITEM_IMPRESSAO, MATAR_SALDO_RESERVA) VALUES (@REQ_MATERIAL,   
	@FILIAL, @MATERIAL, @COR_MATERIAL, @ITEM, @ORDEM_PRODUCAO, @QTDE_ALOCAR, @QTDE_ALOCAR, @CUSTO, @CUSTO, @VALOR, 1, '', @VALOR, NULL, @MATAR_SALDO)  

	--- ATUALIZO PECAS PARTIDAS 
	IF @CLIENTE = 'SOMA'
	BEGIN 

		SELECT @NF_V = NF_ENTRADA, @FORNEC_V = FORNECEDOR  
		FROM PITAGORAS_BD.DBO.XML_PIT_NF_COMPRA                     
		WHERE CODIGO_ESPELHO = @CODIGO_ESPELHO                     
		AND TIPO_XML = 'V'                    

		SELECT @REQ_MAT_V = REQ_MATERIAL 
		FROM ESTOQUE_RET_MAT 
		WHERE NF_ENTRADA = @NF_V
		AND NOME_CLIFOR = @FORNEC_V 
		AND FILIAL = @FILIAL_TRANSITO

		INSERT INTO ESTOQUE_SAI_PECA(REQ_MATERIAL,FILIAL,MATERIAL,PECA,PARTIDA,COR_MATERIAL,ITEM,QTDE,QTDE_AUX) 
		SELECT A.REQ_MATERIAL,A.FILIAL,A.MATERIAL,C.PECA,C.PARTIDA,A.COR_MATERIAL,A.ITEM,A.QTDE,A.QTDE_AUX
		FROM ESTOQUE_SAI1_MAT A
		JOIN MATERIAIS B ON A.MATERIAL = B.MATERIAL 
		JOIN ESTOQUE_RET_PECA C ON C.REQ_MATERIAL = @REQ_MAT_V
		AND C.MATERIAL = A.MATERIAL 
		AND C.COR_MATERIAL = A.COR_MATERIAL 
		WHERE A.REQ_MATERIAL = @REQ_MATERIAL AND A.FILIAL = @FILIAL
		AND B.CTRL_PARTIDAS = 1 

	END 



END TRY    
  
BEGIN CATCH  
	SELECT @CODIGO_RETORNO = 2,   
	@MENSAGEM = 'Erro ao salvar os itens da requisição de saída! Informe o setor de TI. ' + ERROR_MESSAGE()   
  
	GOTO ERROR  
END CATCH   
  
COMMIT TRANSACTION  
  
RETURN   
  
ERROR:  
IF @@TRANCOUNT > 0     
ROLLBACK TRANSACTION           
  
RETURN 
GO


